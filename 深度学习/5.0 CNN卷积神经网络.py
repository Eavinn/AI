"""
卷积就是特征提取器CBAPD：
C：Conv2D 卷积计算
B: BatchNormalization 批标准化
A: Activation 激活
P: MaxPool2D/AveragePooling2D 池化 减少特征数据量
D: Dropout 舍弃
"""

import tensorflow as tf
import os
